#include "libft.h"

char  *ft_strrchr(const char *s, int c)
{
  int   i;
  char  *buff;

  i = 0;
  while (s[i] != '\0')
  {
    if (s[i] == c)
    {
      buff = (char*)&s[i];
    }
    i++;
  }
  return (buff);
}
