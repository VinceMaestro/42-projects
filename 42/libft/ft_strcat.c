char	*ft_strcat(char *restrict s1, const char *restrict s2)
{
	int i1;
	int i2;

	i1 = 0;
	i2 = 0;
	while (s1[i1] != '\0')
	{
		i1++;
	}
	while (s2[i2] != '\0')
	{
		s1[i1] = s2[i2];
		i1++;
		i2++;
	}
	s1[i1] = '\0';
	return (s1);
}
