#include <stdlib.h>

#include <stdio.h>

void	ft_strdel(char **as)
{
	int	i;

	i = 0;
	while (as[i] != 0)
	{
		free(as[i]);
		i++;
	}
	printf("%s\n", (char*)as);
	as = NULL;
}
