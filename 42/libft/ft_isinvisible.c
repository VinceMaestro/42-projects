#include "libft.h"

int	ft_isinvisible(int c)
{
	if (c == (int)' ' || c == (int)'\n' || c == (int)'\t' \
		|| c == (int)'\r' || c == (int)'\v' || c == (int)'\f')
		return (1);
	return (0);
}
